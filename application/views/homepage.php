        <p>The TxWDP is a pilot project developed as part of a study funded by the U.S. Geological Survey (USGS) National Climate Change and Wildlife Science Center. Study collaborators are Texas Tech University and Texas Parks and Wildlife Department. </p>
        <p>This portal provides easy access to historical water quality data for major reservoirs in Texas gathered from various sources including the USGS, the Texas Commission on Environmental Quality, Texas Water Development Board and others. The period of record for these data is as large as January 1965 to May 2012. The portal also provides tools to simplify the complexity of the data for the purpose of analysis, conduct descriptive and other simple statistical tests, and summarize results in tabular and standard graphical output formats. Intended users of this portal are students and researchers interested in the study of water quality and water quality trends in Texas reservoirs.</p>
